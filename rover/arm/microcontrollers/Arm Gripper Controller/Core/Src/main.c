void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{

  // Read encoder pins
  encoderA = HAL_GPIO_ReadPin(EA_GPIO_Port, EA_Pin);
  encoderB = HAL_GPIO_ReadPin(EB_GPIO_Port, EB_Pin);

  // Check which Pin caused the interrupt
  switch(GPIO_Pin){

  	// If the blue button is pressed
    case B1_Pin:
	  printf("oh my\n");
	  break;

	// If encoder A is triggered
    case EA_Pin:
	  if (GPIO_PIN_SET == encoderA)
	  	{
		  if (GPIO_PIN_SET == encoderB) position--;
		  else position++;
//		  // For debugging, printing position at every interrupt
//		  printf("Curr position: %ld\n", position);
	  	}
	  break;

	// If encoder B is triggered
    case EB_Pin:
      if (GPIO_PIN_SET == encoderB)
   	  	{
   		  if (GPIO_PIN_SET == encoderA) position++;
   		  else position--;
//   	  // For debugging, printing position at every interrupt
//   	  printf("Curr position: %ld\n", position);
   	  	}
   	  break;

   	// If interrupt was caused by any other GPIO pin
    default:
      printf("what the fuuuuuuuck?\n");
      break;
  }
}

