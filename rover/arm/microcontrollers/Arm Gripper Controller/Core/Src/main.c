/*
 * void motor_PID(void)
 *
 * Performs the PID loop, sending the appropriate PWM and
 * direction signals on the associated GPIO ports
 *
 * @parameters
 * void
 *
 * @returns
 * void
 *
 */
void motor_PID(void)
{

  // Getting PID output
  int16_t error = goal - position;
  int16_t integral = integral_prior + error;
  int16_t derivative = error - error_prior;
  int32_t output = kP*error + kI*integral + kD*derivative + bias;

  // Setting the direction of motor
  if (0 < output) HAL_GPIO_WritePin(DIR_GPIO_Port, DIR_Pin, GPIO_PIN_SET);
  else HAL_GPIO_WritePin(DIR_GPIO_Port, DIR_Pin, GPIO_PIN_RESET);

  // To prevent integral portion from saturating, putting a limit on output
  if (INT16_MAX <= output)
  {
	integral -= error;
	output = INT16_MAX;
  }
  else if (INT16_MIN >= output)
  {
	integral -= error;
	output = INT16_MIN;
  }

  // Updating the previous error and integral values
  error_prior = error;
  integral_prior = integral;

  // Setting the PWM duty cycle based on the output (PWM frequency = 84 kHz)
  htim2.Instance->CCR3 = (abs(output) * htim2.Init.Period)/INT16_MAX;
  htim2.Instance->CCR1 = (abs(output) * htim2.Init.Period)/INT16_MAX;

//  // Some print statements for potential debugging
//  printf("Curr position: %ld\n", position);
//  printf("PWM output %ld\n\n", output);
}
