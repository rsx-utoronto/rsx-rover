void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{

  // Add the received byte (via UART) to the linear buffer
  linear_buff_add(&linear_buff, uart_rx_buff);

  // Check if the received byte was a '\n', if yes then copy the data
  // from our linear buffer to our processing buffer
  if ('\n' == uart_rx_buff){
	memcpy(rx_buffer, linear_buff.data, BUFF_SIZE);
	buffer_ready = 1;
	linear_buff_flush(&linear_buff);
  }

  // Enable the Interrupt Service Routine again
  HAL_UART_Receive_IT(&huart2, &uart_rx_buff, 1);
}


